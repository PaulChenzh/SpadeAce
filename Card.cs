public class Card {
	private GameObject maJiang;
	private int maJiangId;

	public Card(GameObject maJiang, int maJiangId) {
		this.maJiang = maJiang;
		this.maJiangId = maJiangId;
	}
	
	public GameObject getMaJiang() {
		return maJiang;
	}
	
	public int getMaJiangId() {
		return MaJiangId;
	}
}