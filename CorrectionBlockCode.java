
public class CorrectionBlockCode {
	int c; //total number of codewords
	int k; //number of data codewords
	int r; //error correction  capacity
	
	
	public int getC() {
		return c;
	}
	public void setC(int c) {
		this.c = c;
	}
	public int getK() {
		return k;
	}
	public void setK(int k) {
		this.k = k;
	}
	public int getR() {
		return r;
	}
	public void setR(int r) {
		this.r = r;
	}
}
